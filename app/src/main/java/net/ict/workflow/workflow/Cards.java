package net.ict.workflow.workflow;

class Cards {
    private float max = 8.24f;
    private float zeit = 4.2f;

    public Cards(float max, float zeit) {
        this.max = max;
        this.zeit = zeit;
    }

    public float getMax() {
        return max;
    }

    public float getZeit() {
        return zeit;
    }
}
